      <h1>SmartSeg</h1>
      <p>SmartSeg lets you easily create groups of customers from your database. These groups of customers will help you better understand their interests, and therefore provide them targeted services.</p>
      <p>To start, select values from the form above, where:</p>
      <ul>
        <li><strong>Database</strong> is the name of the database with customer data.</li>
        <li><strong>Num. segments</strong> is the number of segments to be generated. You can choose 'Auto' if no specific number of segments is preferred.</li>
        <li><strong>Features</strong> refers to the set of customer features the system will use to create the segments and visualise the results:</li>
        <ul>
          <li><strong>RFM</strong>, which stands for Recency-Frequency-Monetary Value, considers how active and profitable the customers are.</li>
          <li><strong>Transactions</strong> considers the types and amount of transactions performed by the customer.</li>
          <li><strong>All</strong> considers both transactions and demographic values together.</li>
        </ul>
      </ul>
      <p>Once you have selected values for these, click 'OK' to process it. </p>
      <p>After a few seconds, you will see a summary of the segments created automatically by SmartSeg, where you can click on each specific segment to further explore the result.</p>
